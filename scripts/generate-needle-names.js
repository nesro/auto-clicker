import fs from 'node:fs';
import path from 'node:path';
const NEEDLE_IMAGE_EXTENSIONS = new Set(['.png', '.jpg', '.jpeg']);
function isNeedleImage(file) {
    return NEEDLE_IMAGE_EXTENSIONS.has(path.extname(file).toLowerCase());
}
function toIdentifier(file, used) {
    const stem = path.basename(file, path.extname(file));
    const base = stem
        .replace(/[^a-zA-Z0-9]+/g, '_')
        .replace(/^_+|_+$/g, '')
        .toUpperCase();
    let identifier = base.length > 0 ? base : 'NEEDLE';
    if (/^\d/.test(identifier)) {
        identifier = `NEEDLE_${identifier}`;
    }
    const original = identifier;
    let suffix = 2;
    while (used.has(identifier)) {
        identifier = `${original}_${suffix}`;
        suffix += 1;
    }
    used.add(identifier);
    return identifier;
}
function toNeedleKey(file, used) {
    const stem = path.basename(file, path.extname(file));
    const words = stem
        .split(/[^a-zA-Z0-9]+/g)
        .map((word) => word.toLowerCase())
        .filter(Boolean);
    let key = words
        .map((word, index) => (index === 0 ? word : `${word[0]?.toUpperCase()}${word.slice(1)}`))
        .join('');
    if (key.length === 0) {
        key = 'needle';
    }
    if (/^\d/.test(key)) {
        key = `needle${key[0]?.toUpperCase()}${key.slice(1)}`;
    }
    const original = key;
    let suffix = 2;
    while (used.has(key)) {
        key = `${original}${suffix}`;
        suffix += 1;
    }
    used.add(key);
    return key;
}
function quoted(value) {
    return `'${value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}
function generateNeedleNames(needleDir) {
    const files = fs.readdirSync(needleDir).filter(isNeedleImage).sort();
    const usedIdentifiers = new Set();
    const usedKeys = new Set();
    const entries = files.map((file) => ({
        file,
        identifier: toIdentifier(file, usedIdentifiers),
        key: toNeedleKey(file, usedKeys),
    }));
    return `/* This file is generated by scripts/generate-needle-names.ts. Do not edit directly. */

export const NEEDLE_NAMES = [
${entries.map(({ file }) => `  ${quoted(file)},`).join('\n')}
] as const;

export type NeedleName = (typeof NEEDLE_NAMES)[number];

export const NEEDLE_KEYS = [
${entries.map(({ key }) => `  ${quoted(key)},`).join('\n')}
] as const;

export type NeedleKey = (typeof NEEDLE_KEYS)[number];

export const NEEDLES = {
${entries.map(({ file, identifier }) => `  ${identifier}: ${quoted(file)},`).join('\n')}
} as const satisfies Record<string, NeedleName>;

export const NEEDLE_NAME_BY_KEY = {
${entries.map(({ file, key }) => `  ${key}: ${quoted(file)},`).join('\n')}
} as const satisfies Record<NeedleKey, NeedleName>;
`;
}
const needleDir = path.resolve(process.argv[2] ?? 'obsidian-knight/needles_dung');
const outFile = path.resolve(process.argv[3] ?? 'src/generated/needle-names.ts');
fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, generateNeedleNames(needleDir));
console.log(`generated ${path.relative(process.cwd(), outFile)}`);
